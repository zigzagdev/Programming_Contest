switch ($S) {
    case "SUN":
        echo "7";
        break;
    case "MON":
        echo "6";

        break;
    case "TUE":
        echo "5";

        break;
    case "WED":
        echo "4";

        break;
    case "THU":
        echo "3";

        break;
    case "FRI":
        echo "2";

        break;
    case "SAT":
        echo "1";

        break;
}